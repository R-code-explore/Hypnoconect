<br>
Dans une vision où « tout est relié », je considère tout être humain comme un individu compétent et
répondant de manière logique par rapport à son contexte afin de maintenir un certain équilibre en
adéquation avec sa vision du monde, même si cela peut le faire souffrir. Compétent dans le sens où il met
tout en oeuvre, et généralement de manière inconsciente, pour s’adapter à son environnement.
<br>
<br>
Cependant, quand ces solutions s’avèrent inopérantes et que vous n’êtes plus satisfaits de la relation que
vous entretenez avec votre environnement, un accompagnement peut vous être utile et bénéfique.
<br>
<br>
Tout au long de l’accompagnement, nous collaborons ensemble avec nos compétences, nos valeurs, nos
responsabilités respectives et aussi nos insuffisances. Je vous accompagne pour mettre en lumière vos
ressources et vous permettre ainsi d’ouvrir la voie vers d’autres possibles, d’être acteur de votre
changement, dans un cadre bienveillant et une écoute attentive et active.
<br>
<br>
Pour cela, j’utilise <strong>l’hypnose humaniste</strong> et des outils de <strong>l’approche systémique de l’école Palo Alto</strong> qui me
permettent d’aborder les choses autrement, de vous considérer dans votre globalité et votre singularité.
Ces outils permettent de travailler aussi bien sur le niveau émotionnel que sur les faits concrets.
<br>
