<br>
    <p>
    En chacun de nous, il existe :
<br>
<br>
- Une partie visible, la partie émergée de l’iceberg (10%) : notre conscient. Il fait appel à la logique et
à la raison. Il nous permet de percevoir la réalité avec nos sens dans l’espace et le temps.
Cependant, il ne peut traiter que peu d’informations.
<br>
<br>
- Une partie invisible, la partie immergée de l’iceberg (90%) : notre inconscient, tout ce dont nous ne
sommes pas encore conscient. Il utilise le langage symbolique et métaphorique, et fait appel à notre
intuition. Il s’agit de notre réservoir dans lequel sont stockées toutes nos informations qui nous
constituent, toutes les mémoires de notre vie. C’est à partir de celles-ci que l’on met en place des
automatismes pour s’adapter aux diverses situations de la vie, que l’on forge notre personnalité et
notre perception du monde.
<br>
<br>
Bien que cela puisse paraitre assez étonnant au premier abord, <strong>notre cerveau ne fait pas la distinction entre le
réel et l’imaginaire</strong>.
<br>
<br>
Prenons un exemple, vous êtes seul(e) chez vous, tranquille dans votre lit, sur le point de vous endormir. Il
fait noir. Quand tout à coup, vous entendez des bruits inhabituels qui proviennent de l’entrée. Vous vous
mettez à imaginer divers scénarios, peut être quelqu’un tente de s’introduire chez vous ? Peut-être même
est-il déjà à l’intérieur ? Finalement, pour vous rassurer, vous allez voir ce qui se passe et vous vous rendez
compte que c’est juste le chat qui joue avec les clés. Peu importe la cause du bruit, notre imaginaire a fait le
travail et nous déclenche l’émotion de la peur pour nous prévenir d’un éventuel danger.
<br>
<br>
En <strong>hypnose humaniste</strong>, le praticien considère comme vrai tout ce qui est imaginé par la personne. Il utilise
cette capacité du cerveau et le langage de l’inconscient, <strong>l’intuition</strong>, pour pouvoir saisir les <strong>symboles</strong> qui
contiennent toute la mémoire de vos expériences de vie, toutes les informations qui vous constituent, qui
fait de vous ce que vous êtes aujourd’hui… La parole crée : plus vous décrirez vos ressentis et plus cela
vous paraitra palpable et saisissable.
<br>
<br>
