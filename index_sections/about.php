<p>
Anciennement professeures des écoles, le système scolaire ne me satisfaisait plus et la crise sanitaire a été
le moteur de ma reconversion.</p>
<p class="citations">
<i>« L’esprit intuitif est un don sacré, et l’esprit rationnel est un serviteur fidèle. Nous avons créé une société
qui honore le serviteur et a oublié le don. »</i> A Einstein<p>
<p>
L’ordre, le contrôle, la logique, les statistiques, l’organisation, la rentabilité, la performance, le jugement,
l’évaluation gouvernent et rythme la vie de l’Ecole (et notre société en général) nous obligeant ainsi à
rentrer dans des cases, ne laissant très peu de place à l’imprévisible. De ce fait, le système tend à
« normaliser » tout ce qui nous échappe c’est-à-dire à ramener à la norme, à gommer ce qui nous dépasse,
à empêcher notre imagination de s’exprimer pour trouver des solutions. Il contribue ainsi à engendrer
plusieurs problématiques : <strong>le manque de confiance en soi, l’estime de soi, le manque de motivation et de
créativité, l’anxiété, le stress, les phobies scolaires…</strong> Combien se voient briser leur rêves ? Combien se
sentent stressés à l’approche d’un examen ? Combien n’arrivent pas à trouver de sens dans les
apprentissages ? Combien ne savent pas ce qu’ils vont faire dans leur vie ? etc..</p>
<p class="citations"><i>« La logique vous mènera d'un point A à B. L'imagination vous mènera partout. »</i> A. Einstein</p>
<p>
Evidemment, tout le côté rationnel est nécessaire pour analyser, expliquer, interpréter les situations et
clarifier notre compréhension du monde. Il nous sécurise et réconforte. Mais lorsqu’on arrive à un point où
la norme, l’étiquette ou le costume que le contexte nous impose de porter, ne correspond plus à nos
valeurs et est devenu trop étroit, l’imprévisible peut être une occasion de se recréer et découvrir de
nouvelles possibilités.</p>
<p class="citations"><i>« Il n’existe rien de constant si ce n’est le changement. »</i> Bouddha</p>
<p>Voilà comment la crise sanitaire a été, pour moi, une opportunité de me reconvertir dans le domaine de
l’accompagnement. En me questionnant sur ce qui m’avait amené à choisir le métier de professeur des
écoles, j’ai vu en l’hypnose humaniste et l’approche systémique un moyen de satisfaire mes ambitions :
celles d’accompagner des personnes au maximum de leur potentiel en utilisant leurs ressources et de
m’enrichir humainement. Nous sommes des êtres complexes, uniques et passionnants : chacun de nous a
quelque chose à apprendre de l’autre, ce qui nous pousse, mutuellement, à cheminer et à grandir.</p>
<p class="citations"><i>« Je suis les liens que je tisse avec les autres »</i> - A. Jacquard</p>