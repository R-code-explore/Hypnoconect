    <p class="para title_blog">III. Les principes de la communication</p>

    <p class="para">(D’après Une logique de la communication P. Watzlawick - J.H. Beavin - D.Jackson).</p>

    <p class="para subTitle_blog"><i>1. L’impossibilité de ne pas communiquer</i></p>

    <p class="para">Toute communication influe sur le comportement.</p>

    <div class="innerShSection">
        <p class="para title_blog">Exemple :</p>
        <p class="para">
        Deux individus , A et B, sont assis l’un côté de l’autre à bord d’un train. A souhaite entamer une discussion alors
        que B non. <br>
        Importance du contexte : le train est bondé, A et B ne peuvent pas quitter leur place. <br>
        Lorsque A entreprend l’échange, B n’a que peu de réactions possibles:
        </p>
        <ul>
            <li>Il évite l’échange (il feint de ne pas l’entendre, met ses écouteurs, ferme les yeux…) pour lui
communiquer qu’il ne souhaite pas communiquer</li>
            <li>Il lui dit directement qu’il ne souhaite pas converser. Ce comportement demande un certain courage
puisqu’elle est contraire aux règles de savoir vivre fixées par la société. Une certaine tension s’installera
entre les 2 interlocuteurs.</li>
            <li>Il décide d’accepte r l’échange par politesse.</li>
        </ul>

        <p class="para">Dans tous les cas, il communique un message qui influencera la réaction de A, qui en retour aura un impact sur B
etc.</p>
    </div>
    <p class="para"></p>
    <p class="para subTitle_blog"><i>2. Niveaux de la communication : contenu et relation</i></p>
    <p class="para">Tout message comporte deux sens : d’une part, il communique une information sur des évènements, des opinions, des ressentis, des expériences… D’autre part, il manifeste directement ou indirectement quelque chose sur la relation entre les interlocuteurs.</p>

