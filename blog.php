    <p class="para">L’approche systémique est issue de plusieurs travaux scientifiques (mathématiques, cybernétique, linguistique). Dans les années 1960, L’Ecole Palo Alto, petite ville de Californie, est née. Elle regroupe plusieurs chercheurs tels que G. Bateson, Von Foerster, Satir, Weakland, Haley, Don Jackson, Watzlawick etc. Ils se sont appuyés sur ces différents travaux pour développer de nouveaux concepts et de nouvelles pratiques dans les domaines des relations humaines et de la thérapie. Cette approche s’intéresse à la manière dont le problème se maintient dans le présent en prenant en compte la complexité du contexte et les interactions des personnes concernées par le problème.</p>

    <p class="para title_blog">I. Un système</p>

    <p class="para">Un système est un ensemble d’éléments identifiables, cohérents et reliés les uns des autres par des relations, de telle sorte que si l’une d’elles change, l’ensemble du système s’en trouve modifié. Il répond à une finalité.</p>

    <div class="innerShSection">
        <p class="para title_blog">Exemple :</p>
        <p class="para">
        Le corps humain est un système complexe composé de sous systèmes (nerveux, circulatoire, squelettique,
        musculaire, digestif, endoc rinien etc.) en interaction dans le but de nous maintenir en vie. Il s’autorégule
        pour notre bon fonctionnement. C’est le principe de l’homéostasie.
        </p>
    </div>

    <div class="sep_bar"></div>

    <p class="para title_blog">II. L’importance des interactions, du contexte et de nos perceptions du monde</p>

    <p class="para">Une interaction est le résultat d’un ensemble d’actions et / ou d’affluences réciproques qui peuvent s’établir entre deux individus, objets, phénomènes, éléments ou corps et qui peuvent en modifier la nature ou le comportement.</p>
    <p class="para">Le contexte dans lequel s’effectuent les interactions joue un rôle primordial puisqu’il permet d’en comprendre le sens.</p>

    <div class="innerShSection">
        <p class="para">
        Si on reprend l’exemple du dessus, B marchera comme un crabe tant qu’il y a des gens dans la rue ou qu’il
        portera son pantalon craqué.
        </p>
    </div>

    <p class="para">Nos perceptions jouent un rôle essentiel dans la communication. Selon nos expériences de vie, l’environnement dans lequel nous avons évolué, nous avons, tous, un regard particulier sur les choses qui nous entourent. Nous considérons cette vision comme vraie dans la mesure où elle se vérifie au quotidien.</p>

    <p class="para">Un de mes proches a la conviction qu’il a toujours de la chance dans son malheur. Et devinez quoi ? Cette croyance se vérifie chaque fois qu’une situation répond à cette croyance. Un jour, alors qu’il conduisait pendant sa période de suspension de permis (la seule fois), pas de chance, une dame lui est rentré dedans. La police n’était pas loin. Il risquait de se voir prolonger sa période de suspension et bien il s’en est sorti avec une simple amende et l’assurance de la dame a remboursé la voiture. Incroyable !!</p>

